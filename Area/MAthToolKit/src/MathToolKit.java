
public class MathToolKit {
	
	    void calculate(double a,int b){
	         double result=a+b;
	        System.out.print("addition result:"+result);
	        System.out.println(" maximum:"+Math.max(a, b)+" minimum:"+Math.min(a, b));
	    }
	    void calculate(int b,double c){
	        double result=b-c;
	        System.out.print("subtraction result:"+result);
	        System.out.println(" maximum:"+Math.max(b, c)+" minimum:"+Math.min(b, c));
	    }
	    void calculate(double f,double g){
	        double result=f/g;
	        System.out.print("division result: "+result);
	        System.out.println(" maximum:"+Math.max(f, g)+" minimum:"+Math.min(f, g));
	    }
	    void calculate(int h,int o){
	        int result=h+o;
	        System.out.print("multiplication result: "+result);
	        System.out.println(" maximum:"+Math.max(h,o)+" minimum:"+Math.min(h, o));
	    }
}	    

	


